// receitas dos 3 recheios para o bolo 

let receitaRecheio1 =`
INGREDIENTES DO RECHEIO DE CHOCOLATE:

200g de chocolate meio amargo de boa qualidade, picado
100g de creme de leite de caixinha

Coloque o chocolate picado em uma tigela resistente ao calor.
Aqueça o creme de leite em uma panela pequena até que comece a borbulhar nas bordas. Não deixe ferver.
Despeje o creme de leite quente sobre o chocolate picado. Deixe descansar por um minuto e depois misture delicadamente com uma espátula, até que o chocolate esteja completamente derretido e a mistura fique lisa e brilhante.
Leve à geladeira por pelo menos 4 horas para que a ganache ganhe consistência. Para rechear o bolo, a ganache deve estar firme, mas ainda espalhável.`

let receitaRecheio2 =`
INGREDIENTES DO RECHEIO DE BRIGADEIRO:

1 lata de leite condensado
1 caixinha de creme de leite
4 colheres de sopa de chocolate em pó 50% cacau
1 colher de sopa de manteiga sem sal

Em uma panela, misture o leite condensado, o chocolate em pó e a manteiga.
Leve a mistura ao fogo baixo, mexendo sem parar, até que a manteiga derreta e a mistura comece a desgrudar do fundo da panela.
Quando atingir o ponto de "brigadeiro de colher" (um pouco mais mole que o ponto de enrolar), retire do fogo e adicione o creme de leite. Misture bem até incorporar.
Transfira para um prato, cubra com plástico-filme pressionando a superfície do recheio para evitar a formação de película, e deixe esfriar completamente antes de usar.`

let receitaRecheio3 =`
INGREDIENTES DO RECHEIO DE MOUSSE DE CHOCOLATE:

200g de chocolate meio amargo, picado
300g de creme de leite fresco gelado (ou nata)
2 claras de ovo grandes

Derreta o chocolate em banho-maria ou no micro-ondas, em intervalos de 30 segundos, até ficar completamente liso. Deixe esfriar um pouco.
Bata o creme de leite fresco gelado até obter picos firmes, formando um chantilly.
Em outra tigela, bata as claras em neve até ficarem bem firmes.
Misture o chocolate derretido e já frio ao chantilly, delicadamente, usando uma espátula.
Por fim, adicione as claras em neve à mistura de chocolate e chantilly, também misturando delicadamente com movimentos de baixo para cima, para manter o volume e a leveza da mousse.
Leve à geladeira por pelo menos 4 horas para firmar antes de rechear o bolo.`

// receita da cobertura para o bolo 

let receitaCobertura = `
INGREDIENTES DA COBERTURA DE BRIGADEIRO CREMOSO PARA O BOLO:

1 lata de leite condensado
1 caixinha de creme de leite (200g)
4 colheres de sopa de chocolate em pó 50% cacau
1 colher de sopa de manteiga sem sal

Em uma panela, misture o leite condensado, o chocolate em pó e a manteiga.
Leve ao fogo baixo, mexendo sem parar, até que a mistura comece a desgrudar do fundo da panela, mas sem chegar ao ponto de brigadeiro de enrolar. O objetivo é que fique em um ponto cremoso, perfeito para espalhar.
Retire a panela do fogo e adicione o creme de leite, misturando bem até que ele se incorpore completamente e a cobertura fique homogênea e brilhante.
Deixe a cobertura esfriar um pouco antes de espalhar sobre o bolo. Se for usar em um bolo que já está no prato, pode despejá-la ainda morna para um efeito de "calda vulcão" que escorre pelas laterais. Se preferir uma cobertura mais firme para decorar com bico de confeitar, leve à geladeira por cerca de 2 horas.`

